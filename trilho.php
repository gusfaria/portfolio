                  <p>This project in collaboration with the designer Twyla Mattos was developed during undergrad on my second semester. Its main objective was to design a tool that would help visually impaired children from the most important institute in Brazil for visual impaired, Instituto Benjamin Constant.</p>
                  <p>The result of this project was 2 boards made of MDF, each board could be set with plates made of different textures, such as: sandpaper, sponge, foam, cotton, and other several possibilities; or it also could be set with plates that had highly contrasting colors.</p>
                  <p>This product was made for kids between 0 and 4 years old to help them practice and be evaluated according to the notions of space, direction and touch sensibility, especially, the sensibility on their feet. The greatest challenge was to develop something simple, with low coast and that the teachers could later on reproduce and create more plates.</p>
